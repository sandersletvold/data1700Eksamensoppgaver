CREATE TABLE Bestilling (
    BID INTEGER AUTO_INCREMENT NOT NULL,
    KID INTEGER,
    BORD INTEGER,
    MOTTATT TIMESTAMP,
    VARER VARCHAR(255),
    PRIMARY KEY (BID),
    FOREIGN KEY (KID) REFERENCES Kunde
);

CREATE TABLE Kunde (
    KID INTEGER AUTO_INCREMENT NOT NULL,
    NAVN VARCHAR(50),
    MOBIL VARCHAR(8),
    EPOST VARCHAR(50),
    PASSORD VARCHAR(100),
    PRIMARY KEY (KID)
);

